    pub async fn test_import(endpoint: &TransitEndpoint<'_>) {
        key::import(
            endpoint.client,
            &endpoint.path,
            &endpoint.keys.imported_public_key,
            Some(
                ImportKeyRequest::builder()
                    .public_key(RSA_2048_PUB_PEM.to_owned())
                    .key_type(KeyType::Rsa4096),
            ),
        )
        .await
        .unwrap_err();

        key::import(
            endpoint.client,
            &endpoint.path,
            &endpoint.keys.imported_public_key,
            Some(
                ImportKeyRequest::builder()
                    .public_key(RSA_2048_PUB_PEM.to_owned())
                    .key_type(KeyType::Rsa2048),
            ),
        )
        .await
        .unwrap();

        // Currently, each transit mount gets a 4096-bit RSA key
        // This can be used to send messages only the vault can read.
        // The messaged contains the payload encrypted by an AES key, which itself is
        // encrypted by the RSA pub key.
        let wrapping_key = wrapping_key::get(endpoint.client, &endpoint.path)
            .await
            .unwrap()
            .public_key;

        let ciphertext = wrap_key(&wrapping_key, AES_256_PLAIN);
        key::import(
            endpoint.client,
            &endpoint.path,
            &endpoint.keys.imported_basic,
            Some(
                ImportKeyRequest::builder()
                    .ciphertext(ciphertext.clone())
                    .hash_function(HashFunction::Sha256)
                    .key_type(KeyType::Rsa2048), // wrong type
            ),
        )
        .await
        .unwrap_err();

        key::import(
            endpoint.client,
            &endpoint.path,
            &endpoint.keys.imported_basic,
            Some(
                ImportKeyRequest::builder()
                    .ciphertext(ciphertext)
                    .hash_function(HashFunction::Sha256)
                    .key_type(KeyType::Aes256Gcm96),
            ),
        )
        .await
        .unwrap();

        let ciphertext = wrap_key(&wrapping_key, RSA_2048_PRIVATE_DER);
        key::import(
            endpoint.client,
            &endpoint.path,
            &endpoint.keys.imported_asymmetric,
            Some(
                ImportKeyRequest::builder()
                    .ciphertext(ciphertext)
                    .key_type(KeyType::Rsa2048),
            ),
        )
        .await
        .unwrap();
    }

